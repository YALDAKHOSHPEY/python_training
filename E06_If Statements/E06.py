# برنامه شناسایی سبک زندگی
print("=" * 40)
print("🎯 به برنامه شناسایی شخصیت خوش اومدی!")
print("=" * 40)

# گرفتن اطلاعات از کاربر
name = input("اسمت چیه؟ ")
age = int(input("چند سالته؟ "))
hobby = input("علاقه‌مندی اصلی‌ات چیه؟ (ورزش/برنامه‌نویسی/موسیقی/سفر) ").lower()

print("\n" + "=" * 40)
print(f"خب {name} جان، بیا ببینیم کی هستی...")
print("=" * 40 + "\n")

# بررسی سن
if age < 12:
    print("🧒 تو یه کودک فعالی!")
    if hobby == "ورزش":
        print("⚽ احتمالاً عاشق فوتبالی یا دوچرخه‌سواری!")
    elif hobby == "برنامه‌نویسی":
        print("💻 وای! تو خیلی کوچولو ولی باهوشی! یه نابغه‌ی آینده!")
    else:
        print("🎨 کنجکاوی و بازی کردن بهترین دوست‌هایتن!")

elif age < 18:
    print("🧑‍🎓 تو یه نوجوان پرانرژی‌ای!")
    if hobby == "برنامه‌نویسی":
        print("🚀 عالیه! توی سن تو برنامه‌نویسی یاد گرفتن یعنی آینده‌ی درخشانی داری!")
    elif hobby == "موسیقی":
        print("🎸 چه باحال! گروه موسیقی داری یا تکی می‌زنی؟")
    elif hobby == "ورزش":
        print("🏃 تیم ورزشی داری؟ یا فقط برای تفریح می‌ری؟")
    else:
        print("✈️ عشق سفر و کشف چیزای جدید! ادامه بده!")

elif age < 60:
    print("👨‍💼 تو یه بزرگسال موفق و با تجربه‌ای!")
    
    # شرط تو در تو (Nested condition)
    if hobby == "برنامه‌نویسی" and age < 30:
        print("🔥 جوان و پرشور! به‌عنوان یه برنامه‌نویس، آینده از آن توئه!")
    elif hobby == "برنامه‌نویسی" and age >= 30:
        print("🧠 تو یه برنامه‌نویس کارکشته‌ای! احتمالاً خیلی چیزا رو دیدی!")
    elif hobby == "سفر":
        print("🌍 چه زندگی جذابی! چند کشور رو گشتی؟")
    else:
        print(f"👍 {hobby} انتخاب عالی‌ای برای بالانس زندگی‌اته!")

else:  # age >= 60
    print("👴 تو یه بزرگ‌تر عاقل و با تجربه‌ای!")
    if hobby == "سفر":
        print("🌅 چه خاطرات قشنگی داری از سفرهای گذشته!")
    elif hobby == "موسیقی":
        print("🎵 حتماً خاطرات زیادی با موسیقی داری!")
    else:
        print(f"📚 {hobby} یا شاید هم قصه‌گویی! حتماً کلی داستان جذاب داری!")

# بخش جمع‌بندی با if-else ترکیبی
print("\n" + "-" * 40)
if age < 18:
    print("💪 داری بزرگ می‌شی! از هر لحظه‌ات لذت ببر!")
elif age < 30:
    print("⚡ بهترین دوران زندگی‌ته! انرژی‌ات رو درست مصرف کن!")
elif age < 50:
    print("🏆 دوران اوج! تجربه و انرژی رو باهم داری!")
else:
    print("🍃 آرامش و خردمندی دوران پختگی! بهت افتخار می‌کنیم!")

print("=" * 40)
print("✅ برنامه به پایان رسید. موفق باشی! 🌟")
